Établir le signalement d'un cheval n'est pas à la portée du premier profane venu. <br />
Seules les personnes habilitées ont le droit de délivrer à votre compagnon des papiers en règle. <br />
Il est néanmoins intéressant de se pencher sur les finesses du relevé d'identification.<br />

<h4>Vos papiers !</h4>
De nos jours, tous les chevaux doivent posséder un livret qui indique leurs origines (si elles sont connues) et leur signalement complet : <br />
c'est un peu leur carte d'identité.<br />

<h5>Quand établir un signalement ?</h5>
Si vous faites l'acquisition d'un cheval d'origine inconnue ou dont on a perdu les papiers, ou bien d'un animal importé,<br /> 
vous devez le faire enregistrer auprès de l'organisme compétent de votre pays :<br /> 
en France le S.I.R.E. (Système 'identification répertoriant les Equidés). <br />
Il en est de même si vous faites naître un poulain.<br /> Vous devez contacter cet organisme, ou l'un des haras nationaux, qui vous enverra un formulaire d'identification à compléter. <br />
La procédure d'identification doit suivre des normes techniques précises.<br />
<h5>Une procédure à respecter</h5>
Le signalement du cheval doit être établi avec la plus grande précision, car ce document a pour but de permettre à n'importe qui, à partir du descriptif, d'établir avec certitude l'identité de votre cheval. <br />
Il existe une brochure officielle qui donne toutes les indications nécessaires pour procéder à cette opération dans les normes, c'est-à-dire sans omettre de points importants et en respectant le code international établi par les autorités compétentes.<br />
Le relevé du signalement d'un poulain sous la mère doit être fait par un agent des haras pour les races du pays, par une personne habilitée (vétérinaire, par exemple) pour les autres races . <br />
Sans le visa de la personne habilitée, les papiers du cheval ne sont pas valables.<br />
<h5>Les règles d'un bon signalement</h5>
Pour permettre d'identifier avec certitude un cheval, il faut relever suffisamment d' éléments caractéristiques pour éviter tout risque de confusion.<br />
Mais il ne s'agitpas de chercher à la loupe la moindre caractéristique du cheval.<br />
Lorsqu'on établit le signalement d'un animal sous la mère, des corrections devront, plus tard, lui être apportées .<br />
La robe, notamment, peut se transformer de façon importante. On procède donc à une vérification du signalement lorsque le jeune animal a passé 18 mois.<br />
Une vérification peut également être nécessaire lorsqu'un premier signalement a été mal fait ou est insuffisant, de même lorsqu'un cheval est importé et qu'il faut établir le signalement dans la langue de son pays d'accueil.<br />
<h5>Bon à savoir</h5>
Attention, il faut toujours signaler la robe telle qu'elle apparaît au moment du relevé, sans tenir compte de sa probable évolution (dans le cas d'un poulain gris, par exemple).La robe doit être indiquée précisément avec ses nuances et ses particularités, selon la nomenclature en cours. Toutes les marques doivent également être signalées. Les pièges sont nombreux et il est préférable de se procurer une brochure officielle si l&#8217;on veut les décrire selon les normes.
<br />
<h4>Etablir l'identité d'un cheval</h4>
Pour bien établir l'identité d'un cheval, il faut savoir décrypter tous les signes qui contribuent à le rendre unique.
<br />
<h5>Des moyens scientifiques</h5>
L'identité d'un équidé est déterminée par son espèce (âne ou cheval), sa race, son âge, son origine et, bien sûr, son signalement détaillé. Aujourd&#8217;hui, on dispose de moyens supplémentaires pour certifier l'identité d'un animal. Ainsi, le numéro de tatouage ou de transpondeur et l'hémotype (groupe sanguin) viennent compléter le signalement. Mais c'est surtout le typage ADN qui permet désormais de distinguer avec certitude, malgré la transformation éventuelle des signes extérieurs, un cheval d'un autre.
<br />
<h5>Les éléments du signalement</h5>
Les éléments du signalement qui seront pris en compte dans le relevé sont les suivants :
<br />
<ul>
<li>le sexe (dans le cas d'un mâle, on précise s'il s'agit d'un entier ou d'un hongre) ;</li>
<li>la robe ;</li>
<li>les épis ;</li>
<li>les marques blanches ;</li>
<li>les particularités ;</li>
<li>la couleur des yeux ;</li>
<li>la couleur de la peau ;</li>
<li>la couleur des sabots ;</li>
<li>les marques acquises indélébiles (cicatrices par exemple) ;</li>
<li>si nécessaire, la photographie des châtaignes ;</li>
</ul>
La taille peut être utilisée, chez un cheval adulte, comme détail complémentaire.